import pickle
import numpy as np
import re

with open('models/nlp/problem_analyzer.pkl', 'rb') as f:
    data = pickle.load(f)

pipeline  = data['pipeline']
services  = data['services']
threshold = data['confidence_threshold']

def preprocess(text):
    text = str(text).lower().strip()
    text = re.sub(r'[،,؟?!.]', ' ', text)
    text = re.sub(r'\s+', ' ', text)
    return text

def predict(text):
    clean      = preprocess(text)
    proba      = pipeline.predict_proba([clean])[0]
    classes    = pipeline.classes_
    best_idx   = np.argmax(proba)
    best_cat   = classes[best_idx]
    confidence = float(proba[best_idx])

    if confidence < threshold or best_cat == 'unknown':
        return f"unknown ({confidence:.0%})"

    svc = services[best_cat]
    return f"{best_cat} | {svc['name']} ({confidence:.0%})"

